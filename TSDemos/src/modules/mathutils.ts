function add(a:number, b:number): number{
    return a+b;
}

function sub(a:number, b:number): number{
    return a-b;
}

function mul(a:number, b:number): number{
    return a*b;
}

function divide(a:number, b:number): number{
    return a/b;
}

export {add, sub, mul, divide}
const PI = 3.14;
export default PI;
